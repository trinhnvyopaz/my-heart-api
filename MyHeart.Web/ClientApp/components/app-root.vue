<script lang="ts">
import Vue from "vue";
import { Component } from "vue-property-decorator";
import NavMenu from "@components/nav-menu.vue";
import TopBar from "@components/top-bar.vue";
import EventBus from "@models/EventBus";
import Events from "@models/shared/Events";
import UserApi from "@backend/api/user";
import AuthStore from "@backend/store/authStore.ts";

@Component({
  components: {
    NavMenu,
    TopBar
  }
})
export default class AppRoot extends Vue {
  constructor() {
    super();
  }

    navMenuMini = false;
    idleTime = 0;
    timer = null;
    requesting = false;
    video = false;
    toggleMobileNavMenu = false;

    toggleNavMenu() {
        console.log("toggleNavMenu")
        console.log(!this.navMenuMini)
        this.navMenuMini = !this.navMenuMini;
        EventBus.$emit('open-nav-menu');
    }

    updateMini(newMini: boolean): void {
        console.log("updateMini")
        console.log(newMini)
        this.navMenuMini = newMini;
    }

    mounted() {
        $("#app-root").data("time", this.getTime);
        $("#app-root").data("relogin", this.relogin);

        EventBus.$on(Events.UserLoggedIn, () => {
            console.log("logged in starting")
            this.startTimer();
        });
        EventBus.$on(Events.UserLoggedOut, () => {
            console.log("logged out stopping")
            this.stopTimer();
        });
        EventBus.$on(Events.Navigated, () => {
            this.video = false;
            console.log("navigated - " + this.video)
        });
        EventBus.$on(Events.Video, () => {
            this.video = true;
            console.log("video - " + this.video)
        });

        if (AuthStore.isUserLoggedIn()) {
            console.log("user logged in starting")
            this.startTimer();
        }

        //Zero the idle timer on mouse movement. now turned off as there is just a simple relogin
        /*$("#app-root").mousemove(function (e) {
            if ($("#app-root").data("time")() > 5) {
                var func = $("#app-root").data("relogin");
                func().then(console.log("aaa"));
            }
        });
        $("#app-root").keypress(function (e) {
            if ($("#app-root").data("time")() > 5) {
                var func = $("#app-root").data("relogin");
                func().then(console.log("aaa"));
            }
        });*/

    }

    startTimer() {
        console.log("starting timer");
        if (this.timer != null) {
            this.stopTimer();
        } else {
            this.timer = setInterval(this.timerIncrement, 60000); // 1 minute
        }
    }

    stopTimer() {
        console.log("stopping timer");
        if (this.timer != null) {
            clearTimeout(this.timer);
            this.timer = null;
        }
    }

    getTime() {
        return this.idleTime;
    }

    async relogin() {
        if (!this.requesting) {
            this.requesting = true;
            console.log("relogin");
            var result = await UserApi.refreshLogin();

            if (result.success) {
                AuthStore.saveToken(result.data);
            }

            this.idleTime = 0;
            this.stopTimer();
            this.startTimer();

            this.requesting = false;
        }
    }

    timerIncrement() {
        this.idleTime = this.idleTime + 1;
        console.log(`idle: ${this.idleTime} - video: ${this.video}`)
        //simple token refresh
        if (this.idleTime > 15) {
            this.relogin();
        }
        //setup for auto logout
        /*if (this.idleTime > 5 && this.video) {
            this.relogin();
        }
        if (this.idleTime > 19) { // 20 minutes
            this.stopTimer();
            window.location.reload();
        }*/
    }
}
